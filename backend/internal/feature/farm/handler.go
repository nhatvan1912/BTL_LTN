func (h *Handler) RegisterRoutes(r *gin.RouterGroup) {
	farms := r.Group("/farms")
	{
		farms.POST("", h.Create)
		farms.GET("/:id", h.GetByID)
		farms.PUT("/:id", h.Update)
		farms.DELETE("/:id", h.Delete)
		farms.GET("", h.List)

		farms.GET("/my-farms", h.GetUserFarms)
		farms.GET("/:id/overview", h.GetFarmOverview)
		farms.GET("/:id/structure", h.GetFarmStructure)
		farms.POST("/:id/users", h.AddUserToFarm)
		farms.DELETE("/:id/users/:user_id", h.RemoveUserFromFarm)
	}
}
